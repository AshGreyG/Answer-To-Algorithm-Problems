from typing import List

# We can only consider how to cover the tail of this n × 2 wall:
#
# 1. One 1 × 2 block: F(n - 1)
# 
#   +------+------+------+------+------+
#   |      |      |      |      |  @@  |
#   |      |      |      |      |  @@  |
#   +------+------+------+------+------+
#   |      |      |      |      |  @@  |
#   |      |      |      |      |  @@  |
#   +------+------+------+------+------+
#
# 2. Two 1 × 2 blocks: F(n - 2)
# 
#   +------+------+------+------+------+
#   |      |      |      |  ##  |  ##  |
#   |      |      |      |  ##  |  ##  |
#   +------+------+------+------+------+
#   |      |      |      |  @@  |  @@  |
#   |      |      |      |  @@  |  @@  |
#   +------+------+------+------+------+
#
# 3. Even 1 × 2 blocks to fill gap: F(n - 3) + F(n - 5) + F(n - 7) + ⋯ + F(0) or F(1)
#
#   +------+------+------+------+------+
#   |  ##  |  %%  |  %%  |  ##  |  ##  |
#   |  ##  |  %%  |  %%  |  ##  |  ##  |
#   +------+------+------+------+------+
#   |  ##  |  ##  |  @@  |  @@  |  ##  |
#   |  ##  |  ##  |  @@  |  @@  |  ##  |
#   +------+------+------+------+------+
#
# 4. Odd 1 × 2 blocks to fill gap: F(n - 4) + F(n - 6) + ⋯ + F(0) or F(1) (opposite to 3.)
#
#   +------+------+------+------+------+
#   |      |  %%  |  %%  |  ##  |  ##  |
#   |      |  %%  |  %%  |  ##  |  ##  |
#   +------+------+------+------+------+
#   |      |  %%  |  @@  |  @@  |  ##  |
#   |      |  %%  |  @@  |  @@  |  ##  |
#   +------+------+------+------+------+
#                                                 ##          #
# 5. 6. Similar to 3. and 4., but the tail is not  # but the ##
#
# Finally we have the equation:
#
#   F(n) = F(n - 1) + F(n - 2) + 2 * ∑_{i=0}^{n-3} F(i)

if __name__ == "__main__" :
    n = int(input().rstrip())
    dp : List[int] = [0] * (n + 1)

    if n == 1 :
        print(1)
    elif n == 2 :
        print(2)

    dp[0] = 1
    dp[1] = 1
    dp[2] = 2

    pre = 0
    for i in range(3, n + 1) :
        pre += dp[i - 3]
        dp[i] = (dp[i - 1] + dp[i - 2] + 2 * pre) % 10000

    print(dp[n])